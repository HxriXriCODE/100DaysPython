symbols = ['!', '#', '$', '%', '&', '(', ')', '*', '+']
import random

print("Welcome to the PyPassword Generator!")
nr_letters = int(input("How many letters would you like in your password?\n"))
nr_symbols = int(input(f"How many symbols would you like?\n"))
